//! Requirement commands for unified requirement management
//!
//! Provides Tauri commands for frontend to interact with the unified requirement repository.

use std::collections::BTreeMap;
use std::path::PathBuf;

use crate::error::Result;
use crate::models::requirement::{
    QueryScope, RequirementCreateParams, RequirementExecuteConfig, RequirementItem,
    RequirementPriority, RequirementSource, RequirementStatus, RequirementUpdateParams,
};
use crate::services::unified_requirement_repository::UnifiedRequirementRepository;
use crate::state::AppState;

// ============================================================================
// Helper
// ============================================================================

fn get_repository(state: &AppState, workspace_path: Option<String>) -> UnifiedRequirementRepository {
    let config_dir = state.data_root.lock().unwrap().config_dir();
    let workspace_path = workspace_path
        .filter(|p| !p.trim().is_empty())
        .map(PathBuf::from);
    UnifiedRequirementRepository::new(config_dir, workspace_path)
}

// ============================================================================
// List requirements
// ============================================================================

#[derive(Debug, Clone, serde::Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct ListRequirementsParams {
    /// Query scope: "workspace" or "all"
    #[serde(default)]
    pub scope: String,
    /// Workspace path (optional)
    pub workspace_path: Option<String>,
    /// Status filter (optional)
    pub status: Option<String>,
    /// Priority filter (optional)
    pub priority: Option<String>,
    /// Limit (optional)
    pub limit: Option<u32>,
}

/// List requirements
#[cfg(feature = "tauri-app")]
#[tauri::command]
pub async fn list_requirements(
    params: ListRequirementsParams,
    state: tauri::State<'_, AppState>,
) -> Result<Vec<RequirementItem>> {
    let workspace_path = params.workspace_path.clone();

    let scope = match params.scope.as_str() {
        "all" => QueryScope::All,
        _ => QueryScope::Workspace,
    };

    let repository = get_repository(&state, workspace_path);
    repository.register_workspace().ok();

    let mut requirements = repository.list_requirements(scope)?;

    // Apply filters
    if let Some(status) = params.status {
        if let Ok(status) = parse_status(&status) {
            requirements.retain(|r| r.status == status);
        }
    }

    if let Some(priority) = params.priority {
        if let Ok(priority) = parse_priority(&priority) {
            requirements.retain(|r| r.priority == priority);
        }
    }

    if let Some(limit) = params.limit {
        requirements.truncate(limit as usize);
    }

    Ok(requirements)
}

// ============================================================================
// Create requirement
// ============================================================================

#[derive(Debug, Clone, serde::Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct CreateRequirementParams {
    /// Requirement title
    pub title: String,
    /// Requirement description
    pub description: String,
    /// Priority
    #[serde(default)]
    pub priority: Option<String>,
    /// Tags
    pub tags: Option<Vec<String>>,
    /// Has prototype
    pub has_prototype: Option<bool>,
    /// Generated by
    pub generated_by: Option<String>,
    /// Generator task ID
    pub generator_task_id: Option<String>,
    /// Workspace path
    pub workspace_path: Option<String>,
}

/// Create requirement
#[cfg(feature = "tauri-app")]
#[tauri::command]
pub async fn create_requirement(
    params: CreateRequirementParams,
    state: tauri::State<'_, AppState>,
) -> Result<RequirementItem> {
    let workspace_path = params.workspace_path.clone();

    let repository = get_repository(&state, workspace_path.clone());

    if workspace_path.filter(|p| !p.trim().is_empty()).is_some() {
        repository.register_workspace().ok();
    }

    let priority = params
        .priority
        .and_then(|p| parse_priority(&p).ok())
        .unwrap_or_default();

    let generated_by = params
        .generated_by
        .and_then(|s| parse_source(&s).ok())
        .unwrap_or_default();

    let create_params = RequirementCreateParams {
        title: params.title,
        description: params.description,
        priority: Some(priority),
        tags: params.tags,
        has_prototype: params.has_prototype,
        generated_by: Some(generated_by),
        generator_task_id: params.generator_task_id,
    };

    repository.create_requirement(create_params)
}

// ============================================================================
// Update requirement
// ============================================================================

#[derive(Debug, Clone, serde::Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct UpdateRequirementParams {
    /// Requirement ID
    pub id: String,
    /// Title
    pub title: Option<String>,
    /// Description
    pub description: Option<String>,
    /// Status
    pub status: Option<String>,
    /// Priority
    pub priority: Option<String>,
    /// Tags
    pub tags: Option<Vec<String>>,
    /// Prototype path
    pub prototype_path: Option<String>,
    /// Has prototype
    pub has_prototype: Option<bool>,
    /// Review note
    pub review_note: Option<String>,
    /// Execute log
    pub execute_log: Option<String>,
    /// Execute error
    pub execute_error: Option<String>,
    /// Generated by
    pub generated_by: Option<String>,
    /// Session ID
    pub session_id: Option<String>,
    /// Execute config
    pub execute_config: Option<ExecuteConfigParam>,
    /// Workspace path
    pub workspace_path: Option<String>,
}

#[derive(Debug, Clone, serde::Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct ExecuteConfigParam {
    pub scheduled_at: Option<i64>,
    pub engine_id: Option<String>,
    pub work_dir: Option<String>,
}

/// Update requirement
#[cfg(feature = "tauri-app")]
#[tauri::command]
pub async fn update_requirement(
    params: UpdateRequirementParams,
    state: tauri::State<'_, AppState>,
) -> Result<RequirementItem> {
    let workspace_path = params.workspace_path.clone();

    let repository = get_repository(&state, workspace_path);

    let execute_config = params.execute_config.map(|ec| RequirementExecuteConfig {
        scheduled_at: ec.scheduled_at,
        engine_id: ec.engine_id,
        work_dir: ec.work_dir,
    });

    let update_params = RequirementUpdateParams {
        title: params.title,
        description: params.description,
        status: params.status.and_then(|s| parse_status(&s).ok()),
        priority: params.priority.and_then(|p| parse_priority(&p).ok()),
        tags: params.tags,
        prototype_path: params.prototype_path,
        has_prototype: params.has_prototype,
        review_note: params.review_note,
        execute_config,
        execute_log: params.execute_log,
        execute_error: params.execute_error,
        generated_by: params.generated_by.and_then(|s| parse_source(&s).ok()),
        session_id: params.session_id,
    };

    repository.update_requirement(&params.id, update_params)
}

